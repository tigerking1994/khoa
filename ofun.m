function f = ofun(x, G1, G2, G3, G12, G13, G14, w1, w2)
k = 125;
L = 150;
D = 8;
kHB = 0.85;
kT = 0.75;
kH = 0.91;
z = 3;
Bc = 10;
Gq = 200;
Tq = 120;
Gp = 10;
nC = 3;
E = 4*(10^5);
% G12 = 20;

u = 4000*(x(1)^0.2)*(x(3)^0.3)*(x(2)^0.4)*(x(4)^0.5);
Nt = 0.12*(D^0.65)*(x(3)^0.64)*(x(2)^0.51);
VB = 3000*(x(1)^0.7)*(x(3)^0.1)*(x(2)^0.9)*u;
T = 2.09*(10^9)*(D^0.088)/((x(1)^2.764)*(x(3)^0.219)*(x(2)^0.299)*(x(4)^0.23));
C = k*L*pi*D/(1000*x(1)*kHB*kT*kH*z*x(2)*T)*(Bc*T+(Gq/Tq)+(z*Gp/nC));
Ra = 9.3171*(x(1)^(-0.119))*(x(2)^0.177)*(x(3)^0.252)*(x(4)^0.182)*(u^0.3);
Py = 6724.7*(D^(0.153-0.654))*(x(3)^0.654)*(x(2)^0.829);

ooff = w1*C/G13 + w2*Ra/G14; 
 
c0=[]; 
c0(1) = C - G13;
c0(2) = Ra - G14;
c0(3) = Nt - G1;
c0(4) = VB - G2;
c0(5) = u - G3;
c0(6) = 0.993*Py/E - G12;

for i=1:length(c0)     
    if c0(i)>0         
        c(i)=1;     
    else
        c(i)=0;     
    end
end
penalty = 10^10;           
f = ooff + penalty*sum(c);   
end 

